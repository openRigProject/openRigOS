// openrig-display — SSD1306 OLED driver for the MMDVM hat display.
//
// One-shot mode:  openrig-display "Some message"
//   Renders header + message and exits. Used by boot/shutdown services.
//
// Daemon mode:    openrig-display -daemon
//   Shows a live clock in idle. When an MQTT QSO start event arrives,
//   switches to showing the radio mode and callsign. Returns to clock on end/lost.
//
// Uses raw Linux I2C syscalls; no external dependencies.
package main

import (
	"bufio"
	"encoding/json"
	"fmt"
	"os"
	"os/exec"
	"os/signal"
	"strings"
	"sync"
	"syscall"
	"time"
)

const (
	i2cDev   = "/dev/i2c-1"
	i2cSlave = 0x0703
	oledAddr = 0x3C
	width    = 128
	height   = 64
	pages    = height / 8 // 8 pages of 8 rows each
)

// ── I2C / SSD1306 ────────────────────────────────────────────────────────────

type display struct {
	fd  int
	buf [pages * width]byte
}

func openDisplay() (*display, error) {
	f, err := os.OpenFile(i2cDev, os.O_RDWR, 0)
	if err != nil {
		return nil, fmt.Errorf("open %s: %w", i2cDev, err)
	}
	d := &display{fd: int(f.Fd())}
	if _, _, errno := syscall.Syscall(syscall.SYS_IOCTL, uintptr(d.fd), i2cSlave, oledAddr); errno != 0 {
		return nil, fmt.Errorf("ioctl I2C_SLAVE: %w", errno)
	}
	return d, nil
}

func (d *display) write(b []byte) error {
	_, err := syscall.Write(d.fd, b)
	return err
}

func (d *display) cmd(cmds ...byte) error {
	buf := make([]byte, len(cmds)+1)
	buf[0] = 0x00 // Co=0, D/C#=0 → command stream
	copy(buf[1:], cmds)
	return d.write(buf)
}

func (d *display) init() error {
	return d.cmd(
		0xAE,       // display off
		0xD5, 0x80, // clock divide ratio / oscillator freq
		0xA8, 0x3F, // multiplex ratio (64-1)
		0xD3, 0x00, // display offset = 0
		0x40,       // display start line = 0
		0x8D, 0x14, // charge pump enabled
		0x20, 0x00, // horizontal addressing mode
		0xA1,       // segment remap (hardware-fixed on this module)
		0xC8,       // COM output: remapped
		0xDA, 0x12, // COM pins hardware config
		0x81, 0xCF, // contrast
		0xD9, 0xF1, // pre-charge period
		0xDB, 0x40, // VCOMH deselect level
		0xA4,       // output follows RAM
		0xA6,       // normal display (not inverted)
		0xAF,       // display on
	)
}

func (d *display) clear() {
	for i := range d.buf {
		d.buf[i] = 0
	}
}

// reverseByte reverses the bit order of a byte (flips 8 vertical pixels in a column).
func reverseByte(b byte) byte {
	b = (b&0xF0)>>4 | (b&0x0F)<<4
	b = (b&0xCC)>>2 | (b&0x33)<<2
	b = (b&0xAA)>>1 | (b&0x55)<<1
	return b
}

func (d *display) flush() error {
	// Software 180° rotation: reverse page order, column order, and bit order.
	var rotated [pages * width]byte
	for p := 0; p < pages; p++ {
		for x := 0; x < width; x++ {
			rotated[(pages-1-p)*width+(width-1-x)] = reverseByte(d.buf[p*width+x])
		}
	}
	copy(d.buf[:], rotated[:])

	if err := d.cmd(0x21, 0, 127); err != nil { // column address 0–127
		return err
	}
	if err := d.cmd(0x22, 0, 7); err != nil { // page address 0–7
		return err
	}
	// Write framebuffer in 32-byte chunks (safe for I2C)
	const chunk = 32
	for i := 0; i < len(d.buf); i += chunk {
		end := i + chunk
		if end > len(d.buf) {
			end = len(d.buf)
		}
		pkt := make([]byte, end-i+1)
		pkt[0] = 0x40 // Co=0, D/C#=1 → data stream
		copy(pkt[1:], d.buf[i:end])
		if err := d.write(pkt); err != nil {
			return err
		}
	}
	return nil
}

// ── Font rendering ────────────────────────────────────────────────────────────
// font8x8 is a row-major 8×8 bitmap font for ASCII 0x20–0x7E.
// Each entry is [8]byte; byte 0 = top row, bit7 = leftmost pixel.

// drawChar renders ch at SSD1306 page p, starting column x.
// The font is transposed from row-major to the column-major format the SSD1306 expects.
func (d *display) drawChar(p, x int, ch byte) {
	if ch < 0x20 || ch > 0x7E {
		ch = '?'
	}
	rows := font8x8[ch-0x20]
	for col := 0; col < 8; col++ {
		var colByte byte
		for row := 0; row < 8; row++ {
			if rows[row]&(0x80>>uint(col)) != 0 {
				colByte |= 1 << uint(row)
			}
		}
		if x+(7-col) < width {
			d.buf[p*width+x+(7-col)] = colByte
		}
	}
}

func (d *display) drawString(p, x int, s string) {
	for _, ch := range s {
		if x >= width {
			break
		}
		d.drawChar(p, x, byte(ch))
		x += 8
	}
}

// centeredX returns the x offset to center a string of length n on the display.
func centeredX(n int) int {
	x := (width - n*8) / 2
	if x < 0 {
		x = 0
	}
	return x
}

// drawHLine draws a horizontal line of dots across the full width on page p.
func (d *display) drawHLine(p int) {
	for x := 0; x < width; x++ {
		d.buf[p*width+x] = 0x08 // middle row of page
	}
}

// ── Screen layouts ────────────────────────────────────────────────────────────

// renderHeader draws the "openRig" title and separator (common to all screens).
func (d *display) renderHeader() {
	header := "openRig"
	d.drawString(0, centeredX(len(header)), header)
	d.drawHLine(1)
}

// renderIdle draws the idle clock screen.
//
//	Page 0: "openRig"
//	Page 1: ─────────
//	Page 3: "14:35 UTC"
//	Page 5: "Thu Apr 30"
func (d *display) renderIdle() {
	d.clear()
	d.renderHeader()
	now := time.Now().UTC()
	timeStr := now.Format("15:04 UTC")
	dateStr := now.Format("Mon Jan 02")
	d.drawString(3, centeredX(len(dateStr)), dateStr)
	d.drawString(5, centeredX(len(timeStr)), timeStr)
}

// renderQSO draws the active-QSO screen.
//
//	Page 0: "openRig"
//	Page 1: ─────────
//	Page 2: ">> QSO <<"
//	Page 4: callsign (centered, ≤14 chars)
//	Page 5: mode (centered)
func (d *display) renderQSO(callsign, mode string) {
	d.clear()
	d.renderHeader()
	label := ">> QSO <<"
	d.drawString(2, centeredX(len(label)), label)
	if len(callsign) > 14 {
		callsign = callsign[:14]
	}
	d.drawString(4, centeredX(len(callsign)), callsign)
	d.drawString(5, centeredX(len(mode)), mode)
}

// renderMessage draws the simple header + message layout (one-shot use).
func (d *display) renderMessage(msg string) {
	d.clear()
	d.renderHeader()
	if len(msg) > 16 {
		msg = msg[:16]
	}
	d.drawString(3, centeredX(len(msg)), msg)
}

// ── Daemon ────────────────────────────────────────────────────────────────────

func runDaemon(d *display) {
	var (
		mu        sync.Mutex
		qsoActive bool
		callsign  string
		radioMode string
	)

	render := func() {
		mu.Lock()
		active := qsoActive
		cs := callsign
		rm := radioMode
		mu.Unlock()
		if active {
			d.renderQSO(cs, rm)
		} else {
			d.renderIdle()
		}
		if err := d.flush(); err != nil {
			fmt.Fprintf(os.Stderr, "openrig-display: flush: %v\n", err)
		}
	}

	render()

	// Clock ticker — refresh display every minute, aligned to the minute boundary.
	go func() {
		for {
			now := time.Now()
			next := now.Truncate(time.Minute).Add(time.Minute)
			time.Sleep(time.Until(next))
			mu.Lock()
			active := qsoActive
			mu.Unlock()
			if !active {
				render()
			}
		}
	}()

	// MQTT listener — watch for QSO start/end events from MMDVMHost.
	go func() {
		for {
			cmd := exec.Command("mosquitto_sub", "-h", "localhost", "-t", "#", "-v")
			stdout, err := cmd.StdoutPipe()
			if err != nil {
				time.Sleep(10 * time.Second)
				continue
			}
			if err := cmd.Start(); err != nil {
				time.Sleep(10 * time.Second)
				continue
			}
			scanner := bufio.NewScanner(stdout)
			for scanner.Scan() {
				line := scanner.Text()
				idx := strings.Index(line, " ")
				if idx < 0 {
					continue
				}
				var msg map[string]json.RawMessage
				if err := json.Unmarshal([]byte(line[idx+1:]), &msg); err != nil {
					continue
				}
				for mode, raw := range msg {
					if mode == "MMDVM" || mode == "link" {
						continue
					}
					var ev struct {
						Action   string `json:"action"`
						SourceCS string `json:"source_cs"`
					}
					if err := json.Unmarshal(raw, &ev); err != nil {
						continue
					}
					switch ev.Action {
					case "start":
						if ev.SourceCS == "" {
							continue
						}
						mu.Lock()
						qsoActive = true
						callsign = strings.ToUpper(ev.SourceCS)
						radioMode = strings.ToUpper(mode)
						mu.Unlock()
						render()
					case "end", "lost":
						mu.Lock()
						qsoActive = false
						mu.Unlock()
						render()
					}
				}
			}
			cmd.Wait()
			time.Sleep(5 * time.Second)
		}
	}()

	// Block until SIGTERM or SIGINT.
	sig := make(chan os.Signal, 1)
	signal.Notify(sig, syscall.SIGTERM, syscall.SIGINT)
	<-sig
}

// ── Main ──────────────────────────────────────────────────────────────────────

func main() {
	daemon := len(os.Args) > 1 && os.Args[1] == "-daemon"

	d, err := openDisplay()
	if err != nil {
		fmt.Fprintf(os.Stderr, "openrig-display: %v\n", err)
		if daemon {
			// No display hardware present — exit cleanly so the service doesn't restart.
			os.Exit(0)
		}
		os.Exit(1)
	}
	if err := d.init(); err != nil {
		fmt.Fprintf(os.Stderr, "openrig-display: init: %v\n", err)
		os.Exit(1)
	}

	if daemon {
		runDaemon(d)
		return
	}

	// Clear mode: blank the display and exit.
	if len(os.Args) > 1 && os.Args[1] == "-clear" {
		d.clear()
		if err := d.flush(); err != nil {
			fmt.Fprintf(os.Stderr, "openrig-display: flush: %v\n", err)
			os.Exit(1)
		}
		return
	}

	// One-shot: render a single message and exit.
	msg := strings.Join(os.Args[1:], " ")
	if msg == "" {
		msg = "openRig"
	}
	d.renderMessage(msg)
	if err := d.flush(); err != nil {
		fmt.Fprintf(os.Stderr, "openrig-display: flush: %v\n", err)
		os.Exit(1)
	}
}

